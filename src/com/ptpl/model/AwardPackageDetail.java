package com.ptpl.model;

import java.math.BigDecimal;
import java.util.List;

/**
 * 奖品包的内容
 * @author admin
 *
 */
public class AwardPackageDetail {

	private BigDecimal id;//奖品包内容表id
	
	private BigDecimal apId;//奖品包表id
	
	private Short apAwardType;//奖品包中的奖品类型     1站内虚拟，2站外虚拟，3站外实体
	
	private String awardSeq;//奖品序号   不知有啥用
	
	private Integer awardQTY;//奖品份数
	
	private String awardNO;//奖品编号
	
	private Short awardAttr;//奖品种类
	
	private String awardName;//奖品名称
	
	private Award award;//奖品
	
	private List<AwardPackageDetail> apdList;

	public BigDecimal getId() {
		return id;
	}

	public void setId(BigDecimal id) {
		this.id = id;
	}

	public BigDecimal getApId() {
		return apId;
	}

	public void setApId(BigDecimal apId) {
		this.apId = apId;
	}

	public Short getApAwardType() {
		return apAwardType;
	}

	public void setApAwardType(Short apAwardType) {
		this.apAwardType = apAwardType;
	}

	public String getAwardSeq() {
		return awardSeq;
	}

	public void setAwardSeq(String awardSeq) {
		this.awardSeq = awardSeq;
	}

	public Integer getAwardQTY() {
		return awardQTY;
	}

	public void setAwardQTY(Integer awardQTY) {
		this.awardQTY = awardQTY;
	}

	public String getAwardNO() {
		return awardNO;
	}

	public void setAwardNO(String awardNO) {
		this.awardNO = awardNO;
	}

	public Short getAwardAttr() {
		return awardAttr;
	}

	public void setAwardAttr(Short awardAttr) {
		this.awardAttr = awardAttr;
	}

	public String getAwardName() {
		return awardName;
	}

	public void setAwardName(String awardName) {
		this.awardName = awardName;
	}

	public List<AwardPackageDetail> getApdList() {
		return apdList;
	}

	public void setApdList(List<AwardPackageDetail> apdList) {
		this.apdList = apdList;
	}

	public Award getAward() {
		return award;
	}

	public void setAward(Award award) {
		this.award = award;
	}

	@Override
	public String toString() {
		return "AwardPackageDetail [id=" + id + ", apId=" + apId + ", apAwardType=" + apAwardType + ", awardSeq="
				+ awardSeq + ", awardQTY=" + awardQTY + ", awardNO=" + awardNO + ", awardAttr=" + awardAttr
				+ ", awardName=" + awardName + "]";
	}
	
}
