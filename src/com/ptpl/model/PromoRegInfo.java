package com.ptpl.model;

import java.math.BigDecimal;
import java.util.Date;
/**
 * 推广码注册用户记录(无根注册)
 * @author 作者 xiaoy: 
 * @version 创建时间：2017年1月16日 下午3:39:58 
 *
 */
public class PromoRegInfo {
	/**ID*/
    private BigDecimal id;
    /**推广用户ID*/
    private BigDecimal puserid;
    /**注册用户ID*/
    private BigDecimal ruserid;
    /**注册用户码*/
    private String regusercode;
    /**注册时间*/
    private Date regtime;

    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public BigDecimal getPuserid() {
        return puserid;
    }

    public void setPuserid(BigDecimal puserid) {
        this.puserid = puserid;
    }

    public BigDecimal getRuserid() {
        return ruserid;
    }

    public void setRuserid(BigDecimal ruserid) {
        this.ruserid = ruserid;
    }

    public String getRegusercode() {
        return regusercode;
    }

    public void setRegusercode(String regusercode) {
        this.regusercode = regusercode == null ? null : regusercode.trim();
    }

    public Date getRegtime() {
        return regtime;
    }

    public void setRegtime(Date regtime) {
        this.regtime = regtime;
    }
}