package com.ptpl.model;

import java.math.BigDecimal;
import java.util.Date;

/**
 * 注册活动规则实体类
 * @author zhenglm
 */
public class RegActivityRule {
	/** 主键ID */
    private BigDecimal id;
    /** 活动编号 */
    private String actno;
    /** 活动名称 */
    private String actname;
    /** 活动开始日期 */
    private Date actbtime;
    /** 活动结束日期 */
    private Date actetime;
    /** 是否判断注册时间（0.否，1.是） */
    private Short isconsiderregtime;
    /** 活动状态（0.待完善,1.待执行,2.执行中,3.已完成,4.已停用,5.已作废,6.已过期） */
    private Short status;
    /** 获奖名单是否需要审核（1.是，2.否） */
    private Short isauditalist;
    /** 制表人 */
    private String addman;
    /** 制表时间 */
    private Date addtime;
    /** 备注 */
    private String remark;
    /** 是否为模板（0.否，1.是） */
    private Short istemplet;
    /** 生成方式（1.手动，2.模板） */
    private Short generatetype;
    /** 执行时间 */
    private Date executetime;
    
	public BigDecimal getId() {
		return id;
	}
	public void setId(BigDecimal id) {
		this.id = id;
	}
	public String getActno() {
		return actno;
	}
	public void setActno(String actno) {
		this.actno = actno;
	}
	public String getActname() {
		return actname;
	}
	public void setActname(String actname) {
		this.actname = actname;
	}
	public Date getActbtime() {
		return actbtime;
	}
	public void setActbtime(Date actbtime) {
		this.actbtime = actbtime;
	}
	public Date getActetime() {
		return actetime;
	}
	public void setActetime(Date actetime) {
		this.actetime = actetime;
	}
	public Short getIsconsiderregtime() {
		return isconsiderregtime;
	}
	public void setIsconsiderregtime(Short isconsiderregtime) {
		this.isconsiderregtime = isconsiderregtime;
	}
	public Short getStatus() {
		return status;
	}
	public void setStatus(Short status) {
		this.status = status;
	}
	public Short getIsauditalist() {
		return isauditalist;
	}
	public void setIsauditalist(Short isauditalist) {
		this.isauditalist = isauditalist;
	}
	public String getAddman() {
		return addman;
	}
	public void setAddman(String addman) {
		this.addman = addman;
	}
	public Date getAddtime() {
		return addtime;
	}
	public void setAddtime(Date addtime) {
		this.addtime = addtime;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public Short getIstemplet() {
		return istemplet;
	}
	public void setIstemplet(Short istemplet) {
		this.istemplet = istemplet;
	}
	public Short getGeneratetype() {
		return generatetype;
	}
	public void setGeneratetype(Short generatetype) {
		this.generatetype = generatetype;
	}
	public Date getExecutetime() {
		return executetime;
	}
	public void setExecutetime(Date executetime) {
		this.executetime = executetime;
	}
}