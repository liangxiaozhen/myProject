package com.ptpl.model;

import java.math.BigDecimal;
import java.util.Date;
import java.util.List;

/**
 * @author：liuqh
 * @Description:标的站岗利息设置
 */ 
public class GfundsInt {
    private BigDecimal id;//ID（主键）
    
    private String gfundsintno;//站岗利息编号

    private Short clearmethod;//清算方式（1结标清算，2最后一期还款清算）

    private String ugrade;//会员等级

    private Double minmoney;//分段最低投资金额

    private Double maxmoney;//分段最高投资金额

    private Double quota;//定额补偿金

    private Double dayawardrate;//日奖励费率

    private Double maxcompensate;//最高补偿金额

    private Short istemplet;//是否为模板（0否，1是）

    private BigDecimal tid;//标id
    
    private Short isaudit;//资金清算是否需要审核
    
    private String addman;//添加人
    
    private Date addtime;//设置时间
    
    private String remark;//备注
    
    private List<GfundsInt> gfundsInts;

    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public String getGfundsintno() {
        return gfundsintno;
    }

    public void setGfundsintno(String gfundsintno) {
        this.gfundsintno = gfundsintno == null ? null : gfundsintno.trim();
    }

    public Short getClearmethod() {
        return clearmethod;
    }

    public void setClearmethod(Short clearmethod) {
        this.clearmethod = clearmethod;
    }

    public String getUgrade() {
        return ugrade;
    }

    public void setUgrade(String ugrade) {
        this.ugrade = ugrade;
    }

    public Double getMinmoney() {
        return minmoney;
    }

    public void setMinmoney(Double minmoney) {
        this.minmoney = minmoney;
    }

    public Double getMaxmoney() {
        return maxmoney;
    }

    public void setMaxmoney(Double maxmoney) {
        this.maxmoney = maxmoney;
    }

    public Double getQuota() {
        return quota;
    }

    public void setQuota(Double quota) {
        this.quota = quota;
    }

    public Double getDayawardrate() {
        return dayawardrate;
    }

    public void setDayawardrate(Double dayawardrate) {
        this.dayawardrate = dayawardrate;
    }

    public Double getMaxcompensate() {
        return maxcompensate;
    }

    public void setMaxcompensate(Double maxcompensate) {
        this.maxcompensate = maxcompensate;
    }

    public Short getIstemplet() {
        return istemplet;
    }

    public void setIstemplet(Short istemplet) {
        this.istemplet = istemplet;
    }

	@Override
	public String toString() {
		return "GfundsInt [id=" + id + ", gfundsintno=" + gfundsintno + ", clearmethod=" + clearmethod + ", ugrade="
				+ ugrade + ", minmoney=" + minmoney + ", maxmoney=" + maxmoney + ", quota=" + quota + ", dayawardrate="
				+ dayawardrate + ", maxcompensate=" + maxcompensate + ", istemplet=" + istemplet + ",tid="+tid+",isaudit="+isaudit+"]";
	}

	public BigDecimal getTid() {
		return tid;
	}

	public void setTid(BigDecimal tid) {
		this.tid = tid;
	}

	public Short getIsaudit() {
		return isaudit;
	}

	public void setIsaudit(Short isaudit) {
		this.isaudit = isaudit;
	}

	public List<GfundsInt> getGfundsInts() {
		return gfundsInts;
	}

	public void setGfundsInts(List<GfundsInt> gfundsInts) {
		this.gfundsInts = gfundsInts;
	}

	public String getAddman() {
		return addman;
	}

	public void setAddman(String addman) {
		this.addman = addman;
	}

	public Date getAddtime() {
		return addtime;
	}

	public void setAddtime(Date addtime) {
		this.addtime = addtime;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
    
}