package com.ptpl.model;

import java.math.BigDecimal;
import java.util.Date;

public class OftenLoanList {
    private BigDecimal id;

    private BigDecimal baseid;

    private BigDecimal loanappid;

    private Date addtime;

    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public BigDecimal getBaseid() {
        return baseid;
    }

    public void setBaseid(BigDecimal baseid) {
        this.baseid = baseid;
    }

    public BigDecimal getLoanappid() {
        return loanappid;
    }

    public void setLoanappid(BigDecimal loanappid) {
        this.loanappid = loanappid;
    }

    public Date getAddtime() {
        return addtime;
    }

    public void setAddtime(Date addtime) {
        this.addtime = addtime;
    }

    @Override
    public String toString() {
        return "OftenLoanList{" +
                "id=" + id +
                ", baseid=" + baseid +
                ", loanappid=" + loanappid +
                ", addtime=" + addtime +
                '}';
    }
}