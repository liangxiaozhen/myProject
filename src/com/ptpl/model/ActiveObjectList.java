package com.ptpl.model;

import java.math.BigDecimal;

public class ActiveObjectList {
	
    private BigDecimal id;//业务对象名单id

    private BigDecimal SNLId;//定向名单列表id
    
    private Short isRightOrExcept;//1.包含  2.排除

    private Short nameType;//名单类型（1.大名单、2.小名单、3.用户名、4.会员等级）

    private String nameContent;//名单具体内容

	public BigDecimal getId() {
		return id;
	}

	public void setId(BigDecimal id) {
		this.id = id;
	}

	public BigDecimal getSNLId() {
		return SNLId;
	}

	public void setSNLId(BigDecimal sNLId) {
		SNLId = sNLId;
	}

	public Short getIsRightOrExcept() {
		return isRightOrExcept;
	}

	public void setIsRightOrExcept(Short isRightOrExcept) {
		this.isRightOrExcept = isRightOrExcept;
	}

	public Short getNameType() {
		return nameType;
	}

	public void setNameType(Short nameType) {
		this.nameType = nameType;
	}

	public String getNameContent() {
		return nameContent;
	}

	public void setNameContent(String nameContent) {
		this.nameContent = nameContent;
	}

	public String toString() {
		return "ActiveObjectList [id=" + id + ", SNLId=" + SNLId + ", isRightOrExcept=" + isRightOrExcept
				+ ", nameType=" + nameType + ", nameContent=" + nameContent + "]";
	}

	
}