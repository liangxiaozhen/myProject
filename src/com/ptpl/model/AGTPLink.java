package com.ptpl.model;

import java.math.BigDecimal;

public class AGTPLink {
    private BigDecimal id;

    private Integer proxygrade;

    private BigDecimal agpaid;

    private Short isopen;
    
    private String name;
    public BigDecimal getId() {
        return id;
    }

    public void setId(BigDecimal id) {
        this.id = id;
    }

    public Integer getProxygrade() {
        return proxygrade;
    }

    public void setProxygrade(Integer proxygrade) {
        this.proxygrade = proxygrade;
    }

    public BigDecimal getAgpaid() {
        return agpaid;
    }

    public void setAgpaid(BigDecimal agpaid) {
        this.agpaid = agpaid;
    }

    public Short getIsopen() {
        return isopen;
    }

    public void setIsopen(Short isopen) {
        this.isopen = isopen;
    }

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}
}