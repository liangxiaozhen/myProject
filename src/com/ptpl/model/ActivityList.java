package com.ptpl.model;

import java.math.BigDecimal;
import java.util.Date;

/**
 * 活动列表实体类
 * @author zhenglm
 */
public class ActivityList {
	/** 主键ID */
    private BigDecimal id;
	/** 活动编号 */
    private String actno;
	/** 活动名称 */
    private String actname;
	/** 活动类型（1.注册  2.累投  3.单标  4.首投 5.手动颁奖 6.自主颁奖7.完善资料8.流标补偿） */
    private Short acttype; //
	/** 活动状态（0.待完善，1待执行，2.执行中，3.已完成，4.已停用，5.已作废，6.已过期） */
    private Short status;  //0.待完善  1.待执行  2.执行中  3.已完成   4.已停用   5.已作废   6.已过期
	/** 获奖人审核(1需要审核，2无需审核) */
    private Short islistaudit;
	/** 生成方式（1手动，2模板） */
    private Short generatetype;
	/** 手动执行(1容许，2不容许) */
    private Short allowmanual;
	/** 执行状态（1手动，2系统，3自主，4联动） */
    private Short executestatus;
	/** 执行时间 */
    private Date executetime;
	/** 获奖人次 */
    private BigDecimal awardtimes;
	/** 获奖人数 */
    private BigDecimal awardnumber;
	/** 活动开始时间 */
    private Date actbegintime;
	/** 活动结束时间 */
    private Date actendtime;
	/** 制作人 */
    private String mademan;
	/** 制作时间 */
    private Date madetime;
	/** 备注 */
    private String remark;
    
	public BigDecimal getId() {
		return id;
	}
	public void setId(BigDecimal id) {
		this.id = id;
	}
	public String getActno() {
		return actno;
	}
	public void setActno(String actno) {
		this.actno = actno;
	}
	public String getActname() {
		return actname;
	}
	public void setActname(String actname) {
		this.actname = actname;
	}
	public Short getActtype() {
		return acttype;
	}
	public void setActtype(Short acttype) {
		this.acttype = acttype;
	}
	public Short getStatus() {
		return status;
	}
	public void setStatus(Short status) {
		this.status = status;
	}
	public Short getIslistaudit() {
		return islistaudit;
	}
	public void setIslistaudit(Short islistaudit) {
		this.islistaudit = islistaudit;
	}
	public Short getGeneratetype() {
		return generatetype;
	}
	public void setGeneratetype(Short generatetype) {
		this.generatetype = generatetype;
	}
	public Short getAllowmanual() {
		return allowmanual;
	}
	public void setAllowmanual(Short allowmanual) {
		this.allowmanual = allowmanual;
	}
	public Short getExecutestatus() {
		return executestatus;
	}
	public void setExecutestatus(Short executestatus) {
		this.executestatus = executestatus;
	}
	public Date getExecutetime() {
		return executetime;
	}
	public void setExecutetime(Date executetime) {
		this.executetime = executetime;
	}
	public BigDecimal getAwardtimes() {
		return awardtimes;
	}
	public void setAwardtimes(BigDecimal awardtimes) {
		this.awardtimes = awardtimes;
	}
	public BigDecimal getAwardnumber() {
		return awardnumber;
	}
	public void setAwardnumber(BigDecimal awardnumber) {
		this.awardnumber = awardnumber;
	}
	public Date getActbegintime() {
		return actbegintime;
	}
	public void setActbegintime(Date actbegintime) {
		this.actbegintime = actbegintime;
	}
	public Date getActendtime() {
		return actendtime;
	}
	public void setActendtime(Date actendtime) {
		this.actendtime = actendtime;
	}
	public String getMademan() {
		return mademan;
	}
	public void setMademan(String mademan) {
		this.mademan = mademan;
	}
	public Date getMadetime() {
		return madetime;
	}
	public void setMadetime(Date madetime) {
		this.madetime = madetime;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}