package com.ptpl.controller.huishang.model;

/**
 * 红包批量转账返回的结果类
 */
public class Trqtres {

	private String bank;//银行代号
	private String batch;//批次号
	private String date;//日期
	private String type;//业务类别
	private String cardnnbr;//转入方电子账号
	private Double amount;//入账金额
	private String curr;//币种
	private String result;//处理结果
	private String name;//持卡人姓名
	private String trdrese;//保留域
	private String rese;//第三方保留域
	
	public String getBank() {
		return bank;
	}
	public void setBank(String bank) {
		this.bank = bank;
	}
	public String getBatch() {
		return batch;
	}
	public void setBatch(String batch) {
		this.batch = batch;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCardnnbr() {
		return cardnnbr;
	}
	public void setCardnnbr(String cardnnbr) {
		this.cardnnbr = cardnnbr;
	}
	public Double getAmount() {
		return amount;
	}
	public void setAmount(Double amount) {
		this.amount = amount;
	}
	public String getCurr() {
		return curr;
	}
	public void setCurr(String curr) {
		this.curr = curr;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTrdrese() {
		return trdrese;
	}
	public void setTrdrese(String trdrese) {
		this.trdrese = trdrese;
	}
	public String getRese() {
		return rese;
	}
	public void setRese(String rese) {
		this.rese = rese;
	}
}
